package info.dolezel.jarss.data;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.ManyToOne;

/**
 *
 * @author lubos
 */
@Entity
public class FeedItemEnclosure implements Serializable {
	@Id
    @GeneratedValue(strategy=GenerationType.AUTO)
    private int id;
	
	@ManyToOne(targetEntity=FeedItemData.class)
	private FeedItemData feedItem;
	
	private String url;
	private String type;
	private long length;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public long getLength() {
		return length;
	}

	public void setLength(long length) {
		this.length = length;
	}

	public FeedItemData getFeedItem() {
		return feedItem;
	}

	public void setFeedItem(FeedItemData feedItem) {
		this.feedItem = feedItem;
	}
}
